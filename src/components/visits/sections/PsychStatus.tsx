export function calcAgeYears(birthDate?: string | null): number | null {
  if (!birthDate) return null;
  const b = new Date(birthDate);
  if (isNaN(b.getTime())) return null;
  const now = new Date();
  let age = now.getFullYear() - b.getFullYear();
  const m = now.getMonth() - b.getMonth();
  if (m < 0 || (m === 0 && now.getDate() < b.getDate())) age--;
  return age;
}

export function ageToGroup(age: number | null): PsychAgeGroup {
  if (age === null) return "Д";
  if (age <= 6) return "А";
  if (age <= 10) return "Б";
  if (age <= 14) return "В";
  if (age <= 17) return "Г";
  return "Д";
}

