// ============ BLOCK COMPONENT ============

function CollapsibleBlock({
  title,
  filled,
  defaultOpen = false,
  children,
  preview,
  actions,
}: {
  title: string;
  filled: boolean;
  defaultOpen?: boolean;
  preview?: string;
  actions?: React.ReactNode;
  children: React.ReactNode;
}) {
  const [open, setOpen] = useState(defaultOpen);
  return (
    <div className="rounded-md border bg-card">
      <div className="flex items-center justify-between gap-2 px-3 py-2">
        <div className="flex-1 min-w-0">
          <div className="text-sm font-medium flex items-center gap-2">
            {title}
            {filled && <span className="inline-block w-2 h-2 rounded-full bg-green-500" />}
          </div>
          {!open && preview && (
            <div className="text-xs text-muted-foreground truncate mt-0.5">{preview}</div>
          )}
        </div>
        <div className="flex items-center gap-1 shrink-0">
          {open && actions}
          <Button
            type="button"
            size="sm"
            variant="ghost"
            className="h-7 px-2 text-xs gap-1"
            onClick={() => setOpen((v) => !v)}
          >
            {open ? <><ChevronUp className="h-3 w-3" /> Свернуть</> : <><ChevronDown className="h-3 w-3" /> Развернуть</>}
          </Button>
        </div>
      </div>
      {open && <div className="px-3 pb-3 space-y-3">{children}</div>}
    </div>
  );
}

function AutoTextarea({
  value,
  onChange,
  minHeight = 250,
  placeholder,
}: {
  value?: string;
  onChange: (v: string) => void;
  minHeight?: number;
  placeholder?: string;
}) {
  const ref = useRef<HTMLTextAreaElement>(null);
  useEffect(() => {
    if (ref.current) {
      ref.current.style.height = "auto";
      ref.current.style.height = Math.max(minHeight, ref.current.scrollHeight) + "px";
    }
  }, [value, minHeight]);
  return (
    <Textarea
      ref={ref}
      value={value || ""}
      onChange={(e) => onChange(e.target.value)}
      placeholder={placeholder}
      className="font-mono text-xs leading-relaxed"
      style={{ minHeight }}
    />
  );
}

