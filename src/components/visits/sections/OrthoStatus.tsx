interface Props {
  value?: string;
  onChange: (v: string) => void;
}

export function OrthoStatusSection({ value, onChange }: Props) {
  const [expanded, setExpanded] = useState(false);
  const textareaRef = useRef<HTMLTextAreaElement>(null);
  const hasValue = !!(value && value.trim());

  // Auto-resize textarea
  useEffect(() => {
    if (expanded && textareaRef.current) {
      const el = textareaRef.current;
      el.style.height = "auto";
      el.style.height = Math.max(300, el.scrollHeight) + "px";
    }
  }, [expanded, value]);

  const preview = hasValue
    ? (value as string).split("\n").filter((l) => l.trim()).slice(0, 2).join(" / ")
    : "";

  return (
    <div className="rounded-md border bg-card">
      <div className="flex items-center justify-between gap-2 px-3 py-2">
        <div className="flex-1 min-w-0">
          <div className="text-sm font-medium">Ортопедический статус</div>
          {!expanded && preview && (
            <div className="text-xs text-muted-foreground truncate mt-0.5">{preview}</div>
          )}
        </div>
        <div className="flex items-center gap-1 shrink-0">
          {expanded && (
            <>
              <Button
                type="button"
                size="sm"
                variant="outline"
                className="h-7 px-2 text-xs gap-1"
                onClick={() => onChange(ORTHO_STATUS_TEMPLATE)}
              >
                <Zap className="h-3 w-3" /> Заполнить стандарт
              </Button>
              <Button
                type="button"
                size="sm"
                variant="ghost"
                className="h-7 px-2 text-xs gap-1"
                disabled={!hasValue}
                onClick={() => onChange("")}
              >
                <RotateCcw className="h-3 w-3" /> Сброс
              </Button>
            </>
          )}
          <Button
            type="button"
            size="sm"
            variant="ghost"
            className="h-7 px-2 text-xs gap-1"
            onClick={() => setExpanded((v) => !v)}
          >
            {expanded ? <><ChevronUp className="h-3 w-3" /> Свернуть</> : <><ChevronDown className="h-3 w-3" /> Развернуть</>}
          </Button>
        </div>
      </div>
      {expanded && (
        <div className="px-3 pb-3">
          <Textarea
            ref={textareaRef}
            value={value || ""}
            onChange={(e) => onChange(e.target.value)}
            className="min-h-[300px] font-mono text-xs leading-relaxed"
            placeholder="Опишите ортопедический статус или нажмите «Заполнить стандарт»"
          />
        </div>
      )}
    </div>
  );
}
