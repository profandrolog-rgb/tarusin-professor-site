// Единый источник инструкций «режима голоса» для промптов.
// Используется в написании обзоров, доработке текста, ревью и консолидации в консилиуме статей.

export type VoiceMode = "impersonal" | "own_data" | "authorial";

export function voicePromptBlock(mode: VoiceMode | undefined | null): string {
  switch (mode) {
    case "own_data":
      return `
РЕЖИМ ГОЛОСА: безличный с собственными данными.
- Основной тон — безличный: «показано, что», «данные свидетельствуют», «получены результаты».
- Разрешено использовать «мы», «в нашей серии наблюдений», «по нашим данным» ТОЛЬКО в тех местах, где речь идёт о материалах, реально загруженных автором (то есть где стоит маркер [M#] от собственных материалов).
- Запрещено приписывать автору данные, которых нет в материалах пользователя. Не пиши «по нашим данным» рядом с фактом, взятым из внешней публикации.
- Никаких «я», «мне кажется», обращений «Вы».`;
    case "authorial":
      return `
РЕЖИМ ГОЛОСА: авторский.
- Разрешено «мы», «я», обращения к читателю («вспомним», «обратите внимание»).
- Тон — экспертный, но живой, как в статьях автора для родителей.
- Соблюдай научную честность: субъективные оценки помечай явно.`;
    case "impersonal":
    default:
      return `
РЕЖИМ ГОЛОСА: строго безличный.
- Используй пассивные и безличные обороты: «показано, что», «данные свидетельствуют», «получены результаты», «исследование продемонстрировало».
- Запрещены: «мы», «я», «в нашей серии», «по нашим данным», обращения к читателю.
- Это научный обзор, а не авторская заметка.`;
  }
}
