
CREATE TABLE IF NOT EXISTS public.pathway_texts (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  pathway_id uuid NOT NULL REFERENCES public.pathways(id) ON DELETE CASCADE,
  register text NOT NULL CHECK (register IN ('simple','pro')),
  summary text,
  what_broken text,
  evidence text,
  risks text,
  connections text,
  actions text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (pathway_id, register)
);

GRANT SELECT, INSERT, UPDATE, DELETE ON public.pathway_texts TO authenticated;
GRANT ALL ON public.pathway_texts TO service_role;

ALTER TABLE public.pathway_texts ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Authenticated read pathway texts"
  ON public.pathway_texts FOR SELECT
  TO authenticated USING (true);

CREATE POLICY "Admins manage pathway texts"
  ON public.pathway_texts FOR ALL
  TO authenticated
  USING (public.has_role(auth.uid(), 'admin'::public.app_role))
  WITH CHECK (public.has_role(auth.uid(), 'admin'::public.app_role));

CREATE TRIGGER trg_pathway_texts_updated_at
  BEFORE UPDATE ON public.pathway_texts
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

