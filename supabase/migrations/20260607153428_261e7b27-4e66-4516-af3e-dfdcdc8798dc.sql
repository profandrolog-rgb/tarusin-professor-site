CREATE TABLE public.diet_recommendations (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  diet_type TEXT NOT NULL,
  diet_label TEXT NOT NULL,
  category TEXT NOT NULL,
  item_text TEXT NOT NULL,
  is_recommended BOOLEAN NOT NULL DEFAULT TRUE,
  sort_order INTEGER,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

GRANT SELECT ON public.diet_recommendations TO anon;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.diet_recommendations TO authenticated;
GRANT ALL ON public.diet_recommendations TO service_role;

ALTER TABLE public.diet_recommendations ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Anyone can read diet recommendations"
  ON public.diet_recommendations FOR SELECT
  USING (true);

CREATE POLICY "Admins manage diet recommendations"
  ON public.diet_recommendations FOR ALL
  TO authenticated
  USING (public.has_role(auth.uid(), 'admin'::app_role))
  WITH CHECK (public.has_role(auth.uid(), 'admin'::app_role));

CREATE TRIGGER update_diet_recommendations_updated_at
  BEFORE UPDATE ON public.diet_recommendations
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE INDEX idx_diet_recommendations_type ON public.diet_recommendations(diet_type, sort_order);

