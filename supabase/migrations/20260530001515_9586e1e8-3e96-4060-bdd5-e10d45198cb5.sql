
-- Smart text templates for protocol forms
CREATE TABLE public.visit_text_templates (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  protocol_type text,
  operation_keywords text[],
  day_range text NOT NULL DEFAULT 'any',
  field_key text NOT NULL,
  template_text text NOT NULL,
  label text NOT NULL,
  sort_order int NOT NULL DEFAULT 100,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

GRANT SELECT ON public.visit_text_templates TO authenticated;
GRANT ALL ON public.visit_text_templates TO service_role;

ALTER TABLE public.visit_text_templates ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Anyone authenticated can read templates"
  ON public.visit_text_templates FOR SELECT
  TO authenticated USING (true);

CREATE POLICY "Admins manage visit text templates"
  ON public.visit_text_templates FOR ALL
  TO authenticated
  USING (has_role(auth.uid(), 'admin'::app_role))
  WITH CHECK (has_role(auth.uid(), 'admin'::app_role));

CREATE TRIGGER trg_visit_text_templates_updated_at
  BEFORE UPDATE ON public.visit_text_templates
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

CREATE INDEX idx_vtt_lookup ON public.visit_text_templates(field_key, protocol_type);
CREATE INDEX idx_vtt_keywords ON public.visit_text_templates USING GIN(operation_keywords);

